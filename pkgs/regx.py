import re

REGX = {
    "date": re.compile(
        r"^"
        r"(0[1-9]|[12][0-9]|3[01])"
        r"([/-]?)"
        r"(0[1-9]|1[0-2])"
        r"\2"
        r"(20[0-9][0-9])"
        r"$"
    ),
    "siape": re.compile(r"\b[0-9]{6,}\b"),
    "sigrh": re.compile(
        r"("
        r"([0-9]{6,})"
        r"(?: "
        r"(?:0[1-9]|[12][0-9]|3[01])"
        r"/"
        r"(?:0[1-9]|1[0-2])"
        r"/"
        r"(?:20[0-9][0-9])"
        r"){2,}"
        r")"
    ),
    "seime": re.compile(
        r"documento assinado eletronicamente por ([^,]+), ([^,]+), em"
        r" ("
        r"(?:0[1-9]|[12][0-9]|3[01])"
        r"/"
        r"(?:0[1-9]|1[0-2])"
        r"/"
        r"(?:20[0-9][0-9])"
        r"),"
        r" as ([0-9]{2}:[0-9]{2}),",
        re.MULTILINE,
    ),
}
